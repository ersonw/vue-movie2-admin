          <div class="card-panel-text">
            新增热度
          </div>
          <count-to :start-val="0" :end-val="messages" :duration="3000" class="card-panel-num" />
        </div>
      </div>
    </el-col>
    <el-col :xs="12" :sm="12" :lg="6" class="card-panel-col">
      <div class="card-panel" @click="handleSetLineChartData('purchases')">
        <div class="card-panel-icon-wrapper icon-money">
          <svg-icon icon-class="money" class-name="card-panel-icon" />
        </div>
        <div class="card-panel-description">
          <div class="card-panel-text">
            新增充值
          </div>
          <count-to :start-val="0" :end-val="purchases" :duration="3200" class="card-panel-num" />
        </div>
      </div>
    </el-col>
    <el-col :xs="12" :sm="12" :lg="6" class="card-panel-col">
      <div class="card-panel" @click="handleSetLineChartData('shoppings')">
        <div class="card-panel-icon-wrapper icon-shopping">
          <svg-icon icon-class="shopping" class-name="card-panel-icon" />
        </div>
        <div class="card-panel-description">
          <div class="card-panel-text">
            新增消费
          </div>
          <count-to :start-val="0" :end-val="shoppings" :duration="3600" class="card-panel-num" />
        </div>
      </div>
    </el-col>
  </el-row>
</template>

<script>
import CountTo from 'vue-count-to'

export default {
  components: {
    CountTo
  },
  props: {
    lineChartData: {
      type: Object
    }
  },
  data() {
    return {
      newVisitis: 0,
      messages: 0,
      purchases: 0,
      shoppings: 0
    }
  },
  watch: {
    lineChartData: {
      handler(newV, oldV) {
        if (newV !== oldV) {
          this.init()
        }
      },
      deep: true
    }
  },
  created() {
    this.init()
  },
  methods: {
    handleSetLineChartData(type) {
      this.$emit('handleSetLineChartData', type)
    },
    init() {
      console.log(this.lineChartData)
      this.newVisitis = this.getSumArry(this.lineChartData.newVisitis.actualData)
      this.messages = this.getSumArry(this.lineChartData.messages.actualData)
      this.purchases = this.getSumArry(this.lineChartData.purchases.actualData)
      this.shoppings = this.getSumArry(this.lineChartData.shoppings.actualData)
    },
    getSumArry(list) {
      console.log(typeof list)
      let n = 0
      if (typeof list === Array) {
        for (let i = 0; i < list.length; i++) {
          n = n + list[i]
        }
      }
      return n
    }
  }
}
</script>

